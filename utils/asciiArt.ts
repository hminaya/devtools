const FONTS: Record<FontName, Record<string, string[]>> = {
  block: BLOCK_5,
  banner: BLOCK_4,
  thin: THIN,
};

export interface AsciiArtOptions {
  font: FontName;
  letterSpacing?: number;  // extra spaces between letters (default 1)
  lineSpacing?: number;    // extra blank lines between rows (default 0)
}

const UNKNOWN_ROW = (width: number) => ' '.repeat(width);

export function generateAsciiArt(text: string, opts: AsciiArtOptions = { font: 'block' }): string {
  const font = FONTS[opts.font] ?? FONTS.block;
  const letterSpacing = opts.letterSpacing ?? 1;
  const lineSpacing = opts.lineSpacing ?? 0;
  const separator = ' '.repeat(letterSpacing);

  if (!text) return '';

  const upper = text.toUpperCase();
  // Determine height of the font (all letters same height); fallback 5.
  const height = Object.values(font)[0]?.length ?? 5;

  // Build each row by horizontally concatenating letters.
  const rows: string[] = Array.from({ length: height }, () => '');
  for (let i = 0; i < upper.length; i++) {
    const char = upper[i]!;
    const glyph = font[char] ?? font[' '] ?? Array.from({ length: height }, () => '     ');

    // Pad glyph rows to equal width (visual alignment).
    const widths = glyph.map((r) => r.length);
    const maxWidth = Math.max(...widths);
    const paddedGlyph = glyph.map((r) => r + ' '.repeat(maxWidth - r.length));

    for (let r = 0; r < height; r++) {
      rows[r] = rows[r] + (i > 0 ? separator : '') + paddedGlyph[r];
    }
  }

  return rows.join('\n' + '\n'.repeat(lineSpacing));
}

